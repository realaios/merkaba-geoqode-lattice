// inject-proto-cosray.cjs — protoplanetary-disk + cosmic-ray-shower
"use strict";
var fs = require("fs");
var FILE = "public/cosmos-infinite.html";

var html = fs.readFileSync(FILE, "utf8");
var usesCRLF = html.includes("\r\n");
if (usesCRLF) html = html.replace(/\r\n/g, "\n");
var ok = 0;

// ── 1. HTML ENTITIES ─────────────────────────────────────────────────────────
var HTML_ANCHOR = "      <a-entity neutron-star-surface></a-entity>";
var HTML_INSERT = "\n      <!-- \u2500\u2500 PROTOPLANETARY DISK \u2014 young star, spinning disk, proto-planet clumps \u2500\u2500 -->\n      <a-entity protoplanetary-disk></a-entity>\n      <!-- \u2500\u2500 COSMIC RAY SHOWER \u2014 high-energy particle cascade through atmosphere \u2500\u2500 -->\n      <a-entity cosmic-ray-shower></a-entity>";

if (html.includes("<a-entity protoplanetary-disk>")) {
  console.log("[1/2] already present"); ok++;
} else if (html.includes(HTML_ANCHOR)) {
  html = html.replace(HTML_ANCHOR, HTML_ANCHOR + HTML_INSERT);
  ok++; console.log("[1/2] HTML entities injected");
} else { console.error("[1/2] FAILED — anchor not found"); }

// ── 2. JS COMPONENTS ─────────────────────────────────────────────────────────
var JS_ANCHOR = '      AFRAME.registerComponent("asteroid-belt", {';

function buildComp() {
  var s = "";

  // ── PROTOPLANETARY DISK ──────────────────────────────────────────────────
  s += "      /* ==================================================================\n";
  s += "         PROTOPLANETARY DISK \u2014 T-Tauri star surrounded by planet-forming disk.\n";
  s += "         Position: (-600, -400, -700).\n";
  s += "         Central young star: orange-yellow, pulsating.\n";
  s += "         Disk: 2400 particles in flat rotating disk r=40-180.\n";
  s += "           Inner hot zone orange, mid cool zone amber, outer icy blue.\n";
  s += "         5 proto-planet clumps orbiting at different radii.\n";
  s += "         Twin protostellar outflow jets (perpendicular to disk).\n";
  s += "         Gap in disk where largest proto-planet cleared its orbit.\n";
  s += "         @alignment 8\u219226\u219248:480  @frequency 963  @domain systems-design\n";
  s += "      ================================================================== */\n";
  s += "      AFRAME.registerComponent(\"protoplanetary-disk\", {\n";
  s += "        init: function () {\n";
  s += "          var THREE = AFRAME.THREE;\n";
  s += "          var scene = this.el.sceneEl.object3D;\n";
  s += "          var PI = Math.PI;\n";
  s += "          this._pdRoot = new THREE.Group();\n";
  s += "          this._pdRoot.position.set(-600, -400, -700);\n";
  s += "          scene.add(this._pdRoot);\n";
  s += "\n";
  s += "          /* central young star */\n";
  s += "          this._yStar = new THREE.Mesh(\n";
  s += "            new THREE.SphereGeometry(14, 14, 10),\n";
  s += "            new THREE.MeshBasicMaterial({ color: 0xffcc44 })\n";
  s += "          );\n";
  s += "          this._pdRoot.add(this._yStar);\n";
  s += "          this._starGlow = new THREE.Mesh(\n";
  s += "            new THREE.SphereGeometry(22, 10, 6),\n";
  s += "            new THREE.MeshBasicMaterial({ color: 0xff8833, transparent: true, opacity: 0.25 })\n";
  s += "          );\n";
  s += "          this._pdRoot.add(this._starGlow);\n";
  s += "\n";
  s += "          /* accretion disk — 2400 particles, flat, temperature gradient */\n";
  s += "          var DN = 2400;\n";
  s += "          var dPos = new Float32Array(DN * 3);\n";
  s += "          var dCol = new Float32Array(DN * 3);\n";
  s += "          this._diskAngles = new Float32Array(DN);\n";
  s += "          this._diskRadii  = new Float32Array(DN);\n";
  s += "          var GAP_INNER = 95, GAP_OUTER = 110; /* cleared gap */\n";
  s += "          for (var di = 0; di < DN; di++) {\n";
  s += "            var dr, attempts = 0;\n";
  s += "            do {\n";
  s += "              dr = 40 + Math.random() * 140;\n";
  s += "              attempts++;\n";
  s += "            } while (dr > GAP_INNER && dr < GAP_OUTER && attempts < 20);\n";
  s += "            var da = Math.random() * PI * 2;\n";
  s += "            this._diskAngles[di] = da;\n";
  s += "            this._diskRadii[di]  = dr;\n";
  s += "            dPos[di*3  ] = dr * Math.cos(da);\n";
  s += "            dPos[di*3+1] = (Math.random()-0.5) * (2 + dr * 0.04);\n";
  s += "            dPos[di*3+2] = dr * Math.sin(da);\n";
  s += "            /* temperature colour */\n";
  s += "            var tc = (dr - 40) / 140;\n";
  s += "            if (tc < 0.35) { /* hot inner — orange-red */\n";
  s += "              dCol[di*3  ] = 1.0; dCol[di*3+1] = 0.45 + tc; dCol[di*3+2] = 0.1;\n";
  s += "            } else if (tc < 0.7) { /* warm mid — amber */\n";
  s += "              dCol[di*3  ] = 0.9; dCol[di*3+1] = 0.7;         dCol[di*3+2] = 0.2;\n";
  s += "            } else { /* cool outer — icy blue */\n";
  s += "              dCol[di*3  ] = 0.4; dCol[di*3+1] = 0.6;         dCol[di*3+2] = 1.0;\n";
  s += "            }\n";
  s += "          }\n";
  s += "          var dGeo = new THREE.BufferGeometry();\n";
  s += "          this._dPosAttr = new THREE.BufferAttribute(dPos, 3);\n";
  s += "          dGeo.setAttribute(\"position\", this._dPosAttr);\n";
  s += "          dGeo.setAttribute(\"color\", new THREE.BufferAttribute(dCol, 3));\n";
  s += "          this._pdRoot.add(new THREE.Points(dGeo, new THREE.PointsMaterial({\n";
  s += "            size: 1.8, vertexColors: true,\n";
  s += "            blending: THREE.AdditiveBlending, depthWrite: false, transparent: true, opacity: 0.75\n";
  s += "          })));\n";
  s += "          this._diskN = DN;\n";
  s += "\n";
  s += "          /* 5 proto-planet clumps */\n";
  s += "          this._protos = [];\n";
  s += "          var protoRadii = [55, 75, 102, 130, 158];\n";
  s += "          var protoColors = [0xff6622, 0xffaa33, 0xffddaa, 0xaaccff, 0x99aaee];\n";
  s += "          var protoSizes  = [4, 6, 8, 5, 3];\n";
  s += "          for (var pi2 = 0; pi2 < 5; pi2++) {\n";
  s += "            var pmesh = new THREE.Mesh(\n";
  s += "              new THREE.SphereGeometry(protoSizes[pi2], 8, 6),\n";
  s += "              new THREE.MeshBasicMaterial({ color: protoColors[pi2] })\n";
  s += "            );\n";
  s += "            this._pdRoot.add(pmesh);\n";
  s += "            this._protos.push({ mesh: pmesh, r: protoRadii[pi2], ang: Math.random()*PI*2, speed: 0.08 / Math.sqrt(protoRadii[pi2]/55) });\n";
  s += "          }\n";
  s += "\n";
  s += "          /* outflow jets */\n";
  s += "          var jMat = new THREE.MeshBasicMaterial({ color: 0x88aaff, transparent: true, opacity: 0.5 });\n";
  s += "          var jN = new THREE.Mesh(new THREE.CylinderGeometry(0.8, 3, 160, 6), jMat);\n";
  s += "          jN.position.set(0, 80, 0);\n";
  s += "          this._pdRoot.add(jN);\n";
  s += "          var jS = new THREE.Mesh(new THREE.CylinderGeometry(0.8, 3, 160, 6), jMat);\n";
  s += "          jS.position.set(0, -80, 0);\n";
  s += "          jS.rotation.z = Math.PI;\n";
  s += "          this._pdRoot.add(jS);\n";
  s += "\n";
  s += "          this._pdTime = 0;\n";
  s += "        },\n";
  s += "        tick: function (time, delta) {\n";
  s += "          var dt = Math.min(delta, 200) * 0.001;\n";
  s += "          this._pdTime += dt;\n";
  s += "          var t2 = this._pdTime;\n";
  s += "          /* star pulse */\n";
  s += "          this._starGlow.material.opacity = 0.18 + 0.1 * Math.sin(t2 * 2.3);\n";
  s += "          /* disk rotation — each particle advances by Keplerian speed */\n";
  s += "          var dArr = this._dPosAttr.array;\n";
  s += "          for (var di = 0; di < this._diskN; di++) {\n";
  s += "            var dr = this._diskRadii[di];\n";
  s += "            this._diskAngles[di] += (0.6 / Math.sqrt(dr / 40)) * dt;\n";
  s += "            var da = this._diskAngles[di];\n";
  s += "            dArr[di*3  ] = dr * Math.cos(da);\n";
  s += "            dArr[di*3+2] = dr * Math.sin(da);\n";
  s += "          }\n";
  s += "          this._dPosAttr.needsUpdate = true;\n";
  s += "          /* proto-planets orbit */\n";
  s += "          for (var pi2 = 0; pi2 < this._protos.length; pi2++) {\n";
  s += "            var pp = this._protos[pi2];\n";
  s += "            pp.ang += pp.speed * dt;\n";
  s += "            pp.mesh.position.set(pp.r * Math.cos(pp.ang), 0, pp.r * Math.sin(pp.ang));\n";
  s += "          }\n";
  s += "        },\n";
  s += "      });\n";
  s += "\n";

  // ── COSMIC RAY SHOWER ────────────────────────────────────────────────────
  s += "      /* ==================================================================\n";
  s += "         COSMIC RAY SHOWER \u2014 ultra-high-energy cosmic ray entering atmosphere.\n";
  s += "         Position: (700, 500, 600).\n";
  s += "         Primary ray: bright streak from high above.\n";
  s += "         Cascade: 12 secondary sub-showers branching on contact.\n";
  s += "         Each secondary: 8 tertiary streaks fanning out further.\n";
  s += "         Atmosphere: blue-white glowing ellipsoid below impact point.\n";
  s += "         Cycle: new shower fires every 5 seconds from random angles.\n";
  s += "         @alignment 8\u219226\u219248:480  @frequency 528  @domain code-eng\n";
  s += "      ================================================================== */\n";
  s += "      AFRAME.registerComponent(\"cosmic-ray-shower\", {\n";
  s += "        init: function () {\n";
  s += "          var THREE = AFRAME.THREE;\n";
  s += "          var scene = this.el.sceneEl.object3D;\n";
  s += "          var PI = Math.PI;\n";
  s += "          this._crRoot = new THREE.Group();\n";
  s += "          this._crRoot.position.set(700, 500, 600);\n";
  s += "          scene.add(this._crRoot);\n";
  s += "\n";
  s += "          /* atmosphere ellipsoid below */\n";
  s += "          var atm = new THREE.Mesh(\n";
  s += "            new THREE.SphereGeometry(60, 14, 8),\n";
  s += "            new THREE.MeshBasicMaterial({ color: 0x2244aa, transparent: true, opacity: 0.12 })\n";
  s += "          );\n";
  s += "          atm.scale.y = 0.35;\n";
  s += "          atm.position.y = -60;\n";
  s += "          this._crRoot.add(atm);\n";
  s += "\n";
  s += "          /* pre-build streak pool — primary + secondary + tertiary */\n";
  s += "          this._streaks = [];\n";
  s += "          var strColors = [0xffffff, 0x88ddff, 0x44aaff];\n";
  s += "          var strCounts = [1, 12, 96]; /* primary, secondary, tertiary */\n";
  s += "          for (var tier = 0; tier < 3; tier++) {\n";
  s += "            for (var si = 0; si < strCounts[tier]; si++) {\n";
  s += "              var verts = [new THREE.Vector3(), new THREE.Vector3()];\n";
  s += "              var sGeo = new THREE.BufferGeometry().setFromPoints(verts);\n";
  s += "              var sMat = new THREE.LineBasicMaterial({\n";
  s += "                color: strColors[tier], blending: THREE.AdditiveBlending,\n";
  s += "                depthWrite: false, transparent: true, opacity: 0\n";
  s += "              });\n";
  s += "              var sLine = new THREE.Line(sGeo, sMat);\n";
  s += "              this._crRoot.add(sLine);\n";
  s += "              this._streaks.push({ line: sLine, mat: sMat, geo: sGeo, tier: tier, index: si });\n";
  s += "            }\n";
  s += "          }\n";
  s += "\n";
  s += "          this._crTime   = 0;\n";
  s += "          this._CYCLE    = 5;\n";
  s += "          this._crPhase  = -1;\n";
  s += "          this._crDir    = new THREE.Vector3();\n";
  s += "        },\n";
  s += "        _fireCascade: function () {\n";
  s += "          var PI = Math.PI;\n";
  s += "          /* random incoming direction (mostly from above) */\n";
  s += "          var th = (Math.random() - 0.5) * PI * 0.5;\n";
  s += "          var ph = Math.random() * PI * 2;\n";
  s += "          this._crDir.set(Math.sin(th) * Math.cos(ph), -Math.cos(th), Math.sin(th) * Math.sin(ph));\n";
  s += "          /* impact point somewhere in atmosphere */\n";
  s += "          var ix = (Math.random()-0.5) * 40;\n";
  s += "          var iz = (Math.random()-0.5) * 40;\n";
  s += "          var iy = -30;\n";
  s += "          var pLen = 120;\n";
  s += "          var pStart = new AFRAME.THREE.Vector3(ix - this._crDir.x * pLen, iy - this._crDir.y * pLen, iz - this._crDir.z * pLen);\n";
  s += "          var pEnd   = new AFRAME.THREE.Vector3(ix, iy, iz);\n";
  s += "          var sk = this._streaks;\n";
  s += "          /* primary */\n";
  s += "          var ps = sk.find(function(s2){return s2.tier===0;});\n";
  s += "          if (ps) {\n";
  s += "            var pa = ps.geo.attributes.position;\n";
  s += "            pa.setXYZ(0, pStart.x, pStart.y, pStart.z);\n";
  s += "            pa.setXYZ(1, pEnd.x, pEnd.y, pEnd.z);\n";
  s += "            pa.needsUpdate = true;\n";
  s += "            ps.mat.opacity = 1.0;\n";
  s += "          }\n";
  s += "          /* secondaries — fan out from impact point */\n";
  s += "          var secondaries = sk.filter(function(s2){return s2.tier===1;});\n";
  s += "          for (var si = 0; si < secondaries.length; si++) {\n";
  s += "            var sph = (si / secondaries.length) * PI * 2;\n";
  s += "            var sth = 0.3 + Math.random() * 0.6;\n";
  s += "            var sLen = 30 + Math.random() * 50;\n";
  s += "            var sd = new AFRAME.THREE.Vector3(Math.sin(sth)*Math.cos(sph), -Math.cos(sth)*0.5, Math.sin(sth)*Math.sin(sph)).normalize();\n";
  s += "            var se = new AFRAME.THREE.Vector3(ix + sd.x*sLen, iy + sd.y*sLen, iz + sd.z*sLen);\n";
  s += "            var sa = secondaries[si].geo.attributes.position;\n";
  s += "            sa.setXYZ(0, ix, iy, iz);\n";
  s += "            sa.setXYZ(1, se.x, se.y, se.z);\n";
  s += "            sa.needsUpdate = true;\n";
  s += "            secondaries[si].mat.opacity = 0.7;\n";
  s += "            secondaries[si]._endPt = se;\n";
  s += "          }\n";
  s += "          /* tertiaries — fan from each secondary end */\n";
  s += "          var tertiaries = sk.filter(function(s2){return s2.tier===2;});\n";
  s += "          for (var ti = 0; ti < tertiaries.length; ti++) {\n";
  s += "            var sIdx = Math.floor(ti / 8);\n";
  s += "            var sec  = secondaries[sIdx];\n";
  s += "            var sp   = sec && sec._endPt ? sec._endPt : new AFRAME.THREE.Vector3(ix, iy, iz);\n";
  s += "            var tph  = (ti * 2.7) % (PI * 2);\n";
  s += "            var tth  = 0.6 + Math.random() * 0.7;\n";
  s += "            var tLen = 12 + Math.random() * 25;\n";
  s += "            var td   = new AFRAME.THREE.Vector3(Math.sin(tth)*Math.cos(tph), -0.6, Math.sin(tth)*Math.sin(tph)).normalize();\n";
  s += "            var te   = new AFRAME.THREE.Vector3(sp.x+td.x*tLen, sp.y+td.y*tLen, sp.z+td.z*tLen);\n";
  s += "            var ta   = tertiaries[ti].geo.attributes.position;\n";
  s += "            ta.setXYZ(0, sp.x, sp.y, sp.z);\n";
  s += "            ta.setXYZ(1, te.x, te.y, te.z);\n";
  s += "            ta.needsUpdate = true;\n";
  s += "            tertiaries[ti].mat.opacity = 0.35;\n";
  s += "          }\n";
  s += "        },\n";
  s += "        tick: function (time, delta) {\n";
  s += "          var dt = Math.min(delta, 200) * 0.001;\n";
  s += "          this._crTime += dt;\n";
  s += "          var t2 = this._crTime;\n";
  s += "          var cyclePos = t2 % this._CYCLE;\n";
  s += "          /* fire new cascade */\n";
  s += "          if (cyclePos < 0.08 && this._crPhase < 0) {\n";
  s += "            this._fireCascade();\n";
  s += "            this._crPhase = 0;\n";
  s += "          }\n";
  s += "          if (cyclePos > 0.5) this._crPhase = -1;\n";
  s += "          /* fade out streaks */\n";
  s += "          if (this._crPhase >= 0) {\n";
  s += "            this._crPhase += dt;\n";
  s += "            var fade = Math.max(0, 1 - this._crPhase * 2.5);\n";
  s += "            for (var si = 0; si < this._streaks.length; si++) {\n";
  s += "              var sk2 = this._streaks[si];\n";
  s += "              var baseFade = sk2.tier === 0 ? 1.0 : sk2.tier === 1 ? 0.7 : 0.35;\n";
  s += "              sk2.mat.opacity = baseFade * fade;\n";
  s += "            }\n";
  s += "          }\n";
  s += "        },\n";
  s += "      });\n";
  s += "\n";

  return s;
}

if (html.includes('AFRAME.registerComponent("protoplanetary-disk"')) {
  console.log("[2/2] already present"); ok++;
} else if (html.includes(JS_ANCHOR)) {
  html = html.replace(JS_ANCHOR, buildComp() + JS_ANCHOR);
  ok++; console.log("[2/2] protoplanetary-disk + cosmic-ray-shower injected");
} else { console.error("[2/2] FAILED — JS anchor not found"); }

if (usesCRLF) html = html.replace(/\n/g, "\r\n");
fs.writeFileSync(FILE, html, "utf8");
console.log("Done! file now has", html.split("\n").length, "lines (ok=" + ok + "/2)");
